package com.cuidadopet.ui.theme

import androidx.compose.ui.graphics.Color

// ─────────────────────────────────────────────
// Paleta de cores do CuidadoPet
// Identidade visual: acolhedora, clara e carinhosa
// Referência: app da PETZ (energia similar, cores próprias)
// ─────────────────────────────────────────────

// COR PRINCIPAL — Teal suave (#3AACB8)
// Usada em: barra superior, botões principais, ícones de destaque
// Transmite: saúde, cuidado, confiança
val Teal = Color(0xFF3AACB8)

// Versão clara do Teal — usada em fundos de cards e containers
// Ex: card de medicamento ativo
val TealLight = Color(0xFFB2E8ED)

// Versão escura do Teal — usada em texto sobre fundo teal claro
val TealDark = Color(0xFF007985)

// COR DE DESTAQUE — Âmbar mel (#F4A426)
// Usada em: badges, alertas de atenção, a palavra "Pet" no logo
// Transmite: calor, energia, afeto
val Amber = Color(0xFFF4A426)

// Versão clara do Âmbar — fundos de avisos informativos
val AmberLight = Color(0xFFFFE8B5)

// Versão escura do Âmbar — texto sobre fundo âmbar claro
val AmberDark = Color(0xFFB97700)

// COR DE SUCESSO — Verde-sálvia (#6BC48A)
// Usada quando algo está correto: "tomou o remédio", "comeu o suficiente"
val GreenSage = Color(0xFF6BC48A)

// Versão clara do verde — fundo de indicadores positivos
val GreenSageLight = Color(0xFFC8EDD6)

// COR DE ALERTA — Coral suave (#F07B6B)
// Usada quando algo precisa de atenção: "não tomou", "abaixo do mínimo"
// É mais suave que vermelho para não causar pânico desnecessário
val CoralSoft = Color(0xFFF07B6B)

// Versão clara do coral — fundo de avisos
val CoralSoftLight = Color(0xFFFFD6D0)

// FUNDO — Branco quente (#FAFAF7)
// Levemente amarelado para ser menos frio que branco puro
// Telas com muito branco puro cansam a vista
val WarmWhite = Color(0xFFFAFAF7)

// TEXTO PRINCIPAL — Cinza-carvão (#2C3E50)
// Não é preto puro — preto puro (#000000) tem contraste muito agressivo
// Este tom é mais agradável para leitura prolongada
val CharcoalGray = Color(0xFF2C3E50)

// TEXTO SECUNDÁRIO — cinza médio para legendas, datas, informações auxiliares
val MediumGray = Color(0xFF7F8C8D)

// DIVISORES E BORDAS — cinza muito claro para separar seções sem peso visual
val LightGray = Color(0xFFE8E8E3)
